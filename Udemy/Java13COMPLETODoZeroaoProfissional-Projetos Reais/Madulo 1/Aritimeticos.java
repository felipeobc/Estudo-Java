/**
 * Aritimeticos
 */
public class Aritimeticos {

    public static void main(String[] args) {
        System.out.println(2 + 3);

        int x=3, y=5;

        System.out.println(x + y);
        System.out.println(x - y);
        System.out.println(x * y);
        System.out.println(x / y);

        //Modulo
        System.out.println(x % y);
    }
}